#!/usr/bin/env node
/**
 * Regenerate the dead-code baseline (AE-0152) — the grandfathered allowlist of
 * currently-unused exports/types. DOWN-ONLY: only run this to ratchet the
 * baseline lower as dead code is removed; raising the count is flagged by
 * scripts/ci/check-integrity.sh.
 *
 * Usage: npm run dead-code:baseline
 */
import { writeFileSync } from "node:fs";

import { BASELINE_PATH } from "./dead-code.config.mjs";
import { scanDeadCodeExports } from "./dead-code-scan.mjs";

const findings = scanDeadCodeExports();
const allowlist = findings.map((f) => f.key).sort((a, b) => a.localeCompare(b));

const baseline = {
  $comment:
    "GENERATED by scripts/generate-dead-code-baseline.mjs (npm run dead-code:baseline). " +
    "Do not hand-edit. AE-0152 dead-export ratchet (down-only). Identity = type|file|symbol.",
  count: allowlist.length,
  allowlist,
};

writeFileSync(BASELINE_PATH, JSON.stringify(baseline, null, 2) + "\n");
process.stdout.write(
  `Dead-code baseline written: ${allowlist.length} grandfathered unused exports.\n`,
);
