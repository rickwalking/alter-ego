#!/usr/bin/env python3
"""Normalize legacy carousel image-provider combos to OpenAI (AE-0308).

Prod intentionally has no GEMINI_API_KEY — every carousel image is generated by
OpenAI. This script repairs rows created before the re-route so a future image
re-run resolves a supported provider instead of failing:

* pre-rename values: ``neon_comic`` style → ``comic_neon``
* any ``gemini*`` model (``gemini``, ``gemini-2.5-flash-preview-05-20``, …)
  → ``openai`` (style unchanged when already supported)

Rendered images on disk are untouched — only the (image_model, image_style)
columns change, which affects what a FUTURE re-run resolves. Idempotent: a
second run finds nothing to change and reports zero rows.

Usage (against prod, from ``backend/``)::

    DATABASE_URL=postgresql+asyncpg://... \
        uv run python scripts/repair_image_provider_combos.py [--dry-run]
"""

from __future__ import annotations

import argparse
import asyncio
import os
from dataclasses import dataclass

from sqlalchemy import text
from sqlalchemy.ext.asyncio import AsyncConnection, create_async_engine

from rag_backend.domain.constants import (
    IMAGE_MODEL_OPENAI,
    IMAGE_STYLE_COMIC_NEON,
    SUPPORTED_IMAGE_COMBOS,
)

LEGACY_MODEL_PREFIX = "gemini"
LEGACY_STYLE_NEON_COMIC = "neon_comic"

_SELECT_PROJECTS = text(
    "SELECT id, image_model, image_style FROM carousel_projects"
)
_UPDATE_PROJECT = text(
    "UPDATE carousel_projects SET image_model = :model, image_style = :style "
    "WHERE id = :id"
)
# The prod DB was bootstrapped via create_all, so changing the model's
# server_default does NOT alter the live column — repair it here (postgres
# only; sqlite test DBs neither support nor need it).
_ALTER_MODEL_DEFAULT = text(
    "ALTER TABLE carousel_projects "
    f"ALTER COLUMN image_model SET DEFAULT '{IMAGE_MODEL_OPENAI}'"
)
_POSTGRES_DIALECT = "postgresql"


@dataclass(frozen=True)
class RowRepair:
    """One row's before → after combo."""

    project_id: str
    old_model: str
    old_style: str
    new_model: str
    new_style: str


def normalize_combo(model: str, style: str) -> tuple[str, str]:
    """Map a legacy (model, style) pair to its supported OpenAI equivalent.

    Already-supported combos pass through unchanged, which is what makes the
    script idempotent.
    """
    new_style = IMAGE_STYLE_COMIC_NEON if style == LEGACY_STYLE_NEON_COMIC else style
    new_model = (
        IMAGE_MODEL_OPENAI if model.startswith(LEGACY_MODEL_PREFIX) else model
    )
    return new_model, new_style


async def repair_image_provider_rows(
    conn: AsyncConnection,
    *,
    dry_run: bool = False,
) -> list[RowRepair]:
    """Repair every legacy row; return the list of changes (empty = clean)."""
    rows = (await conn.execute(_SELECT_PROJECTS)).fetchall()
    repairs: list[RowRepair] = []
    for project_id, model, style in rows:
        new_model, new_style = normalize_combo(model, style)
        if (new_model, new_style) == (model, style):
            continue
        repairs.append(
            RowRepair(
                project_id=str(project_id),
                old_model=model,
                old_style=style,
                new_model=new_model,
                new_style=new_style,
            )
        )
        if not dry_run:
            await conn.execute(
                _UPDATE_PROJECT,
                {"model": new_model, "style": new_style, "id": str(project_id)},
            )
    return repairs


def _report(repairs: list[RowRepair], *, dry_run: bool) -> None:
    verb = "would change" if dry_run else "changed"
    for repair in repairs:
        print(
            f"{repair.project_id}: {repair.old_model}/{repair.old_style}"
            f" -> {repair.new_model}/{repair.new_style}"
        )
        if (repair.new_model, repair.new_style) not in SUPPORTED_IMAGE_COMBOS:
            print(
                f"  WARNING: {repair.project_id} still unsupported after repair"
                f" ({repair.new_model}/{repair.new_style}) — inspect manually"
            )
    print(f"{verb}: {len(repairs)} row(s)")


async def _main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="report the rows that would change without writing",
    )
    args = parser.parse_args()

    engine = create_async_engine(os.environ["DATABASE_URL"])
    try:
        async with engine.begin() as conn:
            repairs = await repair_image_provider_rows(conn, dry_run=args.dry_run)
            _report(repairs, dry_run=args.dry_run)
            if engine.dialect.name == _POSTGRES_DIALECT and not args.dry_run:
                await conn.execute(_ALTER_MODEL_DEFAULT)
                print(f"column default: image_model -> '{IMAGE_MODEL_OPENAI}'")
    finally:
        await engine.dispose()


if __name__ == "__main__":
    asyncio.run(_main())
